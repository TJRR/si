<h1>Política de Privacidade</h1>
<p><strong>Sistema de Gestão da Semana de Inovação e do Prêmio de Inovação do TJRR</strong></p>
<p>Última atualização: 07/07/2026</p>

<h2>1. O que é este sistema</h2>
<p>
    Este sistema é mantido pelo Núcleo de Projetos e Inovação (NPI) do Tribunal de Justiça de Roraima (TJRR)
    para gerir a Semana de Inovação e o Prêmio de Inovação do TJRR: inscrição de equipes e ideias, avaliação
    das submissões pelos avaliadores designados e divulgação de resultados.
</p>

<h2>2. Dados pessoais coletados</h2>
<p>Ao se cadastrar ou submeter uma inscrição, podemos coletar:</p>
<ul>
    <li>Nome completo, e-mail e senha (cadastro manual) ou nome e e-mail (login via Google);</li>
    <li>CPF, telefone e vínculo/profissão dos participantes de cada equipe;</li>
    <li>Arquivos enviados (ex.: documento em PDF) e links de vídeo (ex.: YouTube) relacionados à submissão;</li>
    <li>Registros de acesso e ações relevantes no sistema (auditoria).</li>
</ul>

<h2>3. Finalidade do tratamento</h2>
<p>
    Os dados são usados exclusivamente para viabilizar a inscrição, a avaliação e a divulgação de resultados
    do certame, incluindo a comunicação por e-mail sobre o andamento da inscrição (confirmações, mudanças de
    etapa, prazos).
</p>

<h2>4. Login via Google</h2>
<p>
    Ao optar por entrar com sua conta Google, o sistema recebe apenas seu nome e e-mail, fornecidos pelo
    próprio Google mediante sua autorização, para identificar sua conta. Nenhuma outra informação da sua
    conta Google é acessada.
</p>

<h2>5. Quem acessa os dados</h2>
<p>
    O acesso é restrito por perfil: o Administrador tem acesso completo; o perfil Suporte acessa apenas dados
    cadastrais para conferência de inscrições; os Avaliadores acessam somente as submissões a eles designadas
    e apenas as próprias notas — nunca notas ou dados de outros avaliadores.
</p>

<h2>6. Compartilhamento</h2>
<p>
    Os dados não são compartilhados com terceiros, exceto quando exigido por lei ou por ordem de autoridade
    competente.
</p>

<h2>7. Retenção e eliminação</h2>
<p>
    Os dados são mantidos pelo tempo necessário à realização do certame e ao cumprimento de obrigações legais.
    O titular pode solicitar acesso, correção ou eliminação dos seus dados pessoais, nos termos da Lei Geral
    de Proteção de Dados (Lei nº 13.709/2018), pelo contato abaixo.
</p>

<h2>8. Contato</h2>
<p>
    Dúvidas sobre esta política ou solicitações relacionadas aos seus dados pessoais podem ser enviadas para
    <a href="mailto:npi@tjrr.jus.br">npi@tjrr.jus.br</a>.
</p>

