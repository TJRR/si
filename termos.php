    O sistema é oferecido "como está", podendo passar por manutenções programadas. O NPI não se responsabiliza
    por indisponibilidades decorrentes de fatores fora do seu controle.
</p>

<h2>7. Alterações destes termos</h2>
<p>
    Estes Termos podem ser atualizados a qualquer momento; alterações relevantes serão comunicadas pelos canais
    oficiais do certame.
</p>

