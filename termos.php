<h2>1. Sobre o sistema</h2>
<p>
    Este sistema é disponibilizado pelo Núcleo de Projetos e Inovação (NPI) do Tribunal de Justiça de Roraima
    (TJRR) para operacionalizar a inscrição, avaliação e divulgação de resultados da Semana de Inovação e do
    Prêmio de Inovação do TJRR. O uso deste sistema está sujeito a estes Termos e às regras específicas de cada
    edital vigente do certame.
</p>

<h2>2. Cadastro e acesso</h2>
<p>
    O cadastro pode ser feito manualmente (e-mail e senha) ou via login com sua conta Google. Todo cadastro
    novo fica pendente até ser aprovado pelo Administrador do sistema, que pode aprovar ou rejeitar solicitações
    de acesso.
</p>

<h2>3. Responsabilidade pelo conteúdo enviado</h2>
<p>
    O participante é responsável pela veracidade e exatidão das informações e arquivos enviados (dados de
    equipe e participantes, documentos, links de vídeo), bem como por possuir os direitos necessários sobre o
    conteúdo submetido. Submissões podem ser desclassificadas caso descumpram o edital vigente.
</p>

<h2>4. Regras do certame</h2>
<p>
    Critérios de avaliação, prazos, pesos, fórmulas de pontuação e regras de desempate são definidos pelo
    edital de cada edição do certame, publicado separadamente pelo NPI/Inovajurr. Em caso de divergência entre
    este sistema e o edital, prevalece o texto do edital.
</p>

<h2>5. Sigilo da avaliação</h2>
<p>
    As notas lançadas pelos avaliadores são sigilosas: cada avaliador vê apenas as próprias notas; a
    consolidação e o resultado final são de acesso exclusivo do Administrador até a publicação oficial dos
    resultados.
</p>

<h2>6. Disponibilidade</h2>
